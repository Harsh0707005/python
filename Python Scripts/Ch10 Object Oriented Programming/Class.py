# What is PascalCase?

'''
When first letter of each word is written in uppercase it is called PascalCase.
For example: EmployeeName, NumType etc...
'''

# What is camelCase?

'''
When first letter of each word is written in uppercase except first letter of complete word is called camelCase.
For Example: isNumeric, isFloatAndInt etc... 
'''