# Write a Python program to find remainder when a number is divided by 2.

a = input("Enter first number: ")
a = int(a) # Converting 'a' to integer.
b = input("Enter second number: ")
b = int(b) # Converting 'b' to integer.
print("The remainder when first number is divided by the later is", a%b)
# Here '%' symbol helps us to find the remainder when one number is divided by the other