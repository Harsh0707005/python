t = (1, 2, 4, 5, 1, 3)

# Tuple methods

# Some basic tuple methods are

# 1. Count Method
# { Syntax: variable of tuple.count() }
'''
It counts number of times (frequency) a specified value occurs in a tuple.
'''

print(t.count(1))
print(t.count(4))

print("******************************************")

# 2. Index
# { Syntax: variable of tuple.index() }
'''
It will return the index number of the first occurrence of a specified value.
'''

print(t.index(1))
print(t.index(5))