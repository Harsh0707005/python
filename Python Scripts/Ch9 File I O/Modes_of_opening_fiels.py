# # Modes of opening files.
'''

r  -> open for reading
w  -> open for writing 
a  -> open for appending
r+  -> open for reading and writing both i:e updating a file, if file is not existing will raise I/O error
w+  -> open for reading and writing both i:e updating a file, if file is not existing will create. The existing content will be deleted.

To read a binary file "rb" should be written and to read a text file "rt" or "r" is written.
'''