# Strings can be mainly written in three ways:

# a = 'Harsh'
# a = "Harsh"
# a = '''Harsh'''

# Why there are different ways to write a string ?
# It is because if one wants to write a string as:
a = "Harsh's"
print(a)
# so he can use different types of strings else the python may be confused in about what is string and what isn't.

# Or like this:

b = '''Harsh's'''
print(b)

# Or even like this:

c = 'Harsh"s'
print(c)

# So he can use these combinations of strings in order not to mess up with python.