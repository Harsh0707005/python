# # Functions
'''
A function is a group of statements performing a specific tasks.
When a program gets bigger in size and its complexity grows, it becomes difficult for programmers to keep a track on which piece of code is doing what.
A function can be reused by the programmer in a given program any number of times.

Syntax:

def <name of function()>:
    # Body   (This is called function definition)

Example:
'''
def func1():
    print("Hello")
'''
This function can be called any number of times, anywhere in the program.
'''

# # Function Definition
'''
The part containing the exact set of instructions which are executed during a function call.
'''

# # Function Call
'''
Whenever we want to call a function, we put the name of function followed by parenthesis as follows:
'''
func1()  # -> This is called function call.